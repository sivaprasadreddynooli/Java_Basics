package FirstPackage;

public class Ifelse {

	
	public static void main( String[] args)
	{
		
		/* relational operators
		 * ==   equal comparison 
		 * !=  not equal
		 * <   less than
		 * >   greater than
		 * >=  greater than or equal to
		 * <= less than or equal to
		 * 
		 */
		
		
		int x=10;
		
		int y =23;
		
		if ( x == 10)
		{
			System.out.println("the value of x is 10");
		}
		else if ( y == 23)
		{
			System.out.println("the value of y is 23");
		}
		else
		{
			System.out.println("the value of x is not 10 and  y is not 23");
		}
		
	}



}
